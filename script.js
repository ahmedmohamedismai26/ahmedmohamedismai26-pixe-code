// Current language
let currentLang = localStorage.getItem('language') || 'en';

// Translation function
function t(key) {
  return translations[currentLang][key] || key;
}

// Apply translations to page
function applyTranslations() {
  // Update text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[currentLang][key]) {
      el.innerHTML = translations[currentLang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[currentLang][key]) {
      el.placeholder = translations[currentLang][key];
    }
  });

  // Update select options
  document.querySelectorAll('select option[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[currentLang][key]) {
      el.textContent = translations[currentLang][key];
    }
  });

  // Update document direction
  document.documentElement.setAttribute('dir', currentLang === 'ar' ? 'rtl' : 'ltr');
  document.body.setAttribute('dir', currentLang === 'ar' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', currentLang);
}

// Toggle language
function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'ar' : 'en';
  localStorage.setItem('language', currentLang);
  applyTranslations();

  // Update language toggle button
  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    const enSpan = langToggle.querySelector('.lang-en');
    const arSpan = langToggle.querySelector('.lang-ar');
    if (currentLang === 'ar') {
      enSpan.style.opacity = '0';
      enSpan.style.transform = 'translateY(-20px)';
      arSpan.style.opacity = '1';
      arSpan.style.transform = 'translateY(0)';
    } else {
      enSpan.style.opacity = '1';
      enSpan.style.transform = 'translateY(0)';
      arSpan.style.opacity = '0';
      arSpan.style.transform = 'translateY(20px)';
    }
  }
}

// Make showSection available globally
window.showSection = function (sectionId) {
  console.log('Showing section:', sectionId);

  const sections = document.querySelectorAll('.section');
  const navBtns = document.querySelectorAll('.nav-btn');

  // Hide all sections
  sections.forEach(section => {
    section.classList.remove('active');
    console.log('Removed active from:', section.id);
  });

  // Show target section
  const target = document.getElementById(sectionId);
  if (target) {
    target.classList.add('active');
    console.log('Added active to:', sectionId);
  } else {
    console.error('Section not found:', sectionId);
  }

  // Update nav buttons
  navBtns.forEach(btn => {
    btn.classList.remove('active');
    if (btn.dataset.section === sectionId) {
      btn.classList.add('active');
    }
  });

  // Update URL
  history.pushState(null, null, `#${sectionId}`);

  // Close mobile menu when a link is clicked
  closeMobileMenu();
};

// Navbar scroll effect
window.addEventListener('scroll', () => {
  const navbar = document.querySelector('.navbar');
  if (window.scrollY > 50) {
    navbar.classList.add('scrolled');
  } else {
    navbar.classList.remove('scrolled');
  }
});

document.addEventListener('DOMContentLoaded', () => {
  const navBtns = document.querySelectorAll('.nav-btn');
  const sections = document.querySelectorAll('.section');

  // Language Toggle Button
  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    langToggle.addEventListener('click', toggleLanguage);
  }

  // Nav link click handlers
  navBtns.forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      const sectionId = link.dataset.section;
      console.log('Nav clicked:', sectionId);
      if (sectionId) {
        showSection(sectionId);
      }
    });
  });

  // Handle initial hash with a small delay to ensure DOM is ready
  setTimeout(() => {
    const initialHash = window.location.hash.replace('#', '') || 'home';
    showSection(initialHash);
    console.log('Initial section:', initialHash);
  }, 100);

  // Handle browser back/forward
  window.addEventListener('hashchange', () => {
    const hash = window.location.hash.replace('#', '') || 'home';
    showSection(hash);
  });

  console.log('Portfolio initialized 🚀');

  // Hamburger Menu Toggle
  const hamburger = document.getElementById('hamburger');
  const mobileNav = document.querySelector('.nav-links');

  // Create overlay element
  const overlay = document.createElement('div');
  overlay.className = 'nav-overlay';
  document.body.appendChild(overlay);

  if (hamburger && mobileNav) {
    hamburger.addEventListener('click', () => {
      hamburger.classList.toggle('active');
      mobileNav.classList.toggle('active');
      overlay.classList.toggle('active');
      document.body.style.overflow = mobileNav.classList.contains('active') ? 'hidden' : '';
    });

    // Close menu when clicking on overlay
    overlay.addEventListener('click', closeMobileMenu);

    // Close menu when clicking on a nav link
    const mobileNavBtns = mobileNav.querySelectorAll('.nav-btn');
    mobileNavBtns.forEach(btn => {
      btn.addEventListener('click', closeMobileMenu);
    });
  }

  function closeMobileMenu() {
    hamburger.classList.remove('active');
    mobileNav.classList.remove('active');
    overlay.classList.remove('active');
    document.body.style.overflow = '';
  }

  // Theme Toggle
  const themeToggle = document.getElementById('themeToggle');
  if (themeToggle) {
    const themeIcon = themeToggle.querySelector('i');

    // Check for saved theme preference
    const savedTheme = localStorage.getItem('theme');
    if (savedTheme) {
      document.documentElement.setAttribute('data-theme', savedTheme);
      updateThemeIcon(savedTheme, themeIcon);
    }

    themeToggle.addEventListener('click', () => {
      const currentTheme = document.documentElement.getAttribute('data-theme');
      const newTheme = currentTheme === 'dark' ? 'light' : 'dark';

      document.documentElement.setAttribute('data-theme', newTheme);
      localStorage.setItem('theme', newTheme);
      updateThemeIcon(newTheme, themeIcon);
    });

    function updateThemeIcon(theme, icon) {
      if (theme === 'dark') {
        icon.classList.remove('fa-moon');
        icon.classList.add('fa-sun');
      } else {
        icon.classList.remove('fa-sun');
        icon.classList.add('fa-moon');
      }
    }
  }

  // Image upload functionality
  const imageUpload = document.getElementById('imageUpload');
  const profileImage = document.getElementById('profileImage');

  if (imageUpload && profileImage) {
    imageUpload.addEventListener('change', function (e) {
      const file = e.target.files[0];
      if (file) {
        const reader = new FileReader();
        reader.onload = function (event) {
          profileImage.src = event.target.result;
        };
        reader.readAsDataURL(file);
      }
    });
  }

  // WhatsApp Integration
  document.getElementById('sendWhatsApp').addEventListener('click', (e) => {
    e.preventDefault();

    // Get form values
    const name = document.getElementById('name').value;
    const email = document.getElementById('email').value;
    const phone = document.getElementById('phone').value;
    const subject = document.getElementById('subject').value;
    const service = document.getElementById('service').value;
    const message = document.getElementById('message').value;

    // Validate required fields
    if (!name || !email || !subject || !message) {
      alert('Please fill in all required fields');
      return;
    }

    // Create WhatsApp message
    const whatsappMessage = `Hello, I'm ${name}%0A%0AEmail: ${email}%0APhone: ${phone || 'Not provided'}%0A%0ASubject: ${subject}%0AService: ${service || 'Not specified'}%0A%0AMessage:%0A${message}`;

    // WhatsApp API URL
    const whatsappURL = `https://wa.me/201026874332?text=${whatsappMessage}`;

    // Open WhatsApp
    window.open(whatsappURL, '_blank');
  });
});